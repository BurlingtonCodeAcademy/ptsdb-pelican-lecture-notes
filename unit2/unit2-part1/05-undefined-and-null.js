
// There are two special value-types in JS

// 1. NULL
// think of null as "no value, but I expected there to be no value so it's ok"

let x = null
console.log('the type of x is ' + typeof x)


// 2. UNDEFINED
// think of undefined as "oops! no value"

let y = undefined
console.log('the type of y is ' + typeof y)



